public class Ocorrencia {
    private int idOcorrencia;
    private String dscOcorrencia;
    private Double cpfUsuario;
    private String nomeSupervisor;
    private String placaVeiculo;
    private String dataOcorrencia;
    private String horaOcorrencia;

    public int getIdOcorrencia() {
        return idOcorrencia;
    }

    public void setIdOcorrencia(int idOcorrencia) {
        this.idOcorrencia = idOcorrencia;
    }

    public String getDscOcorrencia() {
        return dscOcorrencia;
    }

    public void setDscOcorrencia(String dscOcorrencia) {
        this.dscOcorrencia = dscOcorrencia;
    }

    public Double getCpfUsuario() {
        return cpfUsuario;
    }

    public void setCpfUsuario(Double cpfUsuario) {
        this.cpfUsuario = cpfUsuario;
    }

    public String getNomeSupervisor() {
        return nomeSupervisor;
    }

    public void setNomeSupervisor(String nomeSupervisor) {
        this.nomeSupervisor = nomeSupervisor;
    }

    public String getPlacaVeiculo() {
        return placaVeiculo;
    }

    public void setPlacaVeiculo(String placaVeiculo) {
        this.placaVeiculo = placaVeiculo;
    }

    public String getDataOcorrencia() {
        return dataOcorrencia;
    }

    public void setDataOcorrencia(String dataOcorrencia) {
        this.dataOcorrencia = dataOcorrencia;
    }

    public String getHoraOcorrencia() {
        return horaOcorrencia;
    }

    public void setHoraOcorrencia(String horaOcorrencia) {
        this.horaOcorrencia = horaOcorrencia;
    }
    
    
}
